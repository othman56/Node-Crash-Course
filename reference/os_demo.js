const os = require("os");

// To get platform
console.log(os.platform());

// To get cpu Architechture
console.log(os.arch());

// to get cpu core info
console.log(os.cpus());

// To get free memory
console.log(os.freemem());

// To get total memory
console.log(os.totalmem());

// To get home directory
console.log(os.homedir());

//  To get uptime
console.log(os.uptime());
